#Faça um Programa que peça dois números e imprima o maior deles

n1 = float(input("\nDigite um valor para N1: "))
n2 = float(input("\nDigite um valor para N2: "))

print("\nN1 = ", n1)
print("N2 = ", n2)

if n1 > n2:
    print("\nN1 é o maior numero.\n")
else:
    print("\nN2 é o maior numero.\n")    
   
